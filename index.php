    <h1>Est-ce que le télétravail est pérenne pour les salariés ?</h1>
    <p>
      Le 11 mars 2020, OMS (l’Organisation mondiale de la santé) a identifié la nouvelle épidémie de coronavirus de
      pandémie qui a complètement bouleversé l’économie mondiale et le monde du travail à une échelle sans précédent.
      Face à cette nouvelle crise sanitaire, les gouvernements du monde entier sont obligés de prendre la situation très
      au sérieux et de se préparer à la première vague d’urgence sanitaire en prenant un certain nombre de mesures
      drastiques, notamment le confinement au niveau national.
    </p>
    <p>
      En France, les mesures de confinement ont été mises en place à trois reprises. De nombreux travailleurs ont été
      invités à pratiquer le télétravail à leur domicile, si leurs activités le permettaient. N’ayant pas de choix, les
      entreprises qui ne l’avaient jamais essayé auparavant l’ont fait de même. Durant la pandémie, le télétravail s’est
      avéré être une mesure importante de la poursuite des activités. Aujourd’hui, malgré que l’histoire des
      quarantaines est terminée, le travail à domicile reste toujours une mesure très recommandée par le gouvernement
      Macron.
    </p>
    <p>
      Pourtant, le télétravail n’est pas un nouveau mode émergent à l’ère de COVID19&#8239;: les premières signes de ce
      concept
      datent bien avant, dans les années 50, mais son application est relativement faible&#8239;: en 2017, selon les chiffres
      de
      l'INSEE, seuls 3 % des salariés le pratiquent au moins un jour par semaine. On ne lui reconnaissait habituellement
      que les avantages&#8239;: meilleur équilibre entre la vie professionnelle et la vie personnelle, réduction des trajets
      entre le domicile et l’entreprise, efficacité accrue des salariés sur leurs tâches ou encore diminution des
      consommation des ressources (par exemple l’électricité) dans les locaux...
    </p>
    <p>
      Avec la propagation de la COVID19, l’application de la manière courante ou obligatoire du télétravail sensibilise
      davantage les gens et leur fait constater que ce dernier peut aussi comporter des risques, tels un sentiment
      d’isolement sociale, notamment pour les personnes vivant seules et un sentiment d’être à l’écart des autres
      employés
      ou de l’entreprise…
    </p>
    <p>
      Aujourd’hui, le télétravail est devenu l’un des sujets de controverse les plus parlés dans notre vie sociale ou
      privée. Les entreprises, les salariés et les pouvoirs publics, trois acteurs principaux du télétravail, portent
      chaque partie leurs arguments dans ce débat infini&#8239;: est-ce que le télétravail est pérenne pour les salariés ?...
    </p>
    <p>
      A la suite de la lecture, nous allons vous présenter plus précisément le télétravail&#8239;: tout d’abord, nous allons
      vous expliquer sa définition et son développement chronologique. Ensuite nous allons vous apporter les principaux
      arguments pour et contre dans ce débat.
    </p>

    <div>
      <h3 class="medium-title">Qu’est-ce que c’est le télétravail ?</h3>

      <p>
        Le télétravail, un mot qu’on ne peut pas éviter de nos jours depuis la crise sanitaire en 2020. Mais est-ce que
        vous
        connaissez vraiment le télétravail ? Quelle sont sa définition et ses conditions ?
      </p>
      <p>
        En France, la notion du télétravail est introduite dans le Code du travail en 2012. L’Article L1222-9 du Code du
        travail indique que « …Le télétravail désigne toute forme d'organisation du travail dans laquelle un travail qui
        aurait également pu être exécuté dans les locaux de l'employeur, est effectué par un salarié hors de ces locaux
        de
        façon volontaire, en utilisant les technologies de l'information et de la communication. »
      </p>
      <p>
        Pourtant, les études sur le télétravail datent bien avant. En 1993, Dans un rapport gouvernemental intitulé « Le
        télétravail en France, situation actuelle, perspectives de développement et aspects juridiques », une première
        définition officielle du télétravail a été déjà donnée&#8239;:
      </p>
      <div>
        <blockquote>
          « Le télétravail est une modalité d'organisation ou d'exécution d'un travail exercé à titre habituel, par une
          personne physique, dans les conditions suivantes&#8239;: d'une part, ce travail s'effectue à distance, c'est-à-dire
          hors
          des abords immédiats de l'endroit où le résultat de ce travail est attendu ; en dehors de toute possibilité
          physique
          pour le donneur d'ordre de surveiller l'exécution de la prestation par le télétravailleur ; d'autre part, ce
          travail
          s'effectue au moyen de l'outil informatique et/ou des outils de télécommunication, y compris au moyen de
          systèmes
          informatiques de communication à distance&#8239;: des données utiles à la réalisation du travail demandé et/ou du
          travail
          réalisé ou en cours de réalisation. »
        </blockquote>
      </div>
      <p>
        Quelle que soit l’évolution de la définition pendant ces 20 ans, on peut toujours constater deux principes
        essentiels du télétravail&#8239;:
      </p>
      <div>
        <ul>
          <li>
            est effectué hors les locaux des employeurs, et ne se limite pas à « travail à domicile »&#8239;: on peut travailler
            depuis un lieu de confiance professionnel (ERP, télécentres, espace de co-working...), un lieu non
            professionnel
            (hôtel, café, restaurant...) ou éventuellement de façon nomade (transport en commun du quotidien, avion,
            bateau…).
          </li>
          <li>
            en utilisant les TIC (technologies de l'information et de la communication)&#8239;: Internet, téléphone, ordinateurs
            fixes, portables… etc.
          </li>
        </ul>
      </div>
      <p>
        (A distinguer avec un médecin qui se déplace au domicile du patient ou un agent immobilier qui accompagne son
        client
        pendant la visite des biens. Ces activités ne sont pas considérées comme le télétravail.)
      </p>
