    <div>
      <h3 class="medium-title">Historique chronologique du télétravail</h3>
      <p>Les années 50&#8239;: le concept de télétravail</p>
      <p>
        Les premiers signes du télétravail apparaît en 1950. Norbert Wiener (mathématicien américain, 1894 - 1964) a
        réussi à superviser, depuis l’Europe, l’avancement de la construction d’un bâtiment aux États-Unis en utilisant
        des moyens de transmission de données. Ce premier exemple du télétravail dans le monde a attiré beaucoup de
        chercheurs qui s’intéressent à ce nouveau mode.
      </p>
      <p>Les années 70&#8239;: l’arrivée du fax numérique</p>
      <p>
        Le télétravail se fait mieux connaître dans les années 70 grâce à l’invention des premiers fax et des
        téléphones.
        Aux Etats-Unis, le terme « Telework » apparaît pour la première fois en 1972 dans un article du « Washington
        Post
        ».
      </p>
      <p>
        En France, le concept de télétravail est apparu pour la première fois dans un rapport sur l'informatisation de
        la
        société, remis au Président de la République par Simon Nora et Alain Minc en 1978.
      </p>
      <p>Les années 90&#8239;: le développement explosif du télétravail grâce à Internet</p>
      <p>
        Le développement des NTIC (Nouvelles Technologies de l’Information et de la Communication) dans les années 1990
        a
        largement facilité les conditions du télétravail, et qui permet par la suite d’intègre de façon définitive le
        télétravail comme nouvelle méthode de travail.
      </p>
      <p>
        En France, une première définition du télétravail a été donné en 1993 dans le cadre d’une mission portant sur
        l’écriture de l’histoire du télétravail a été confiée à Thierry Breton, dirigeant de la société informatique
        Bull,
        par le Premier ministre de l’époque, Édouard Balladur. Seulement deux ans plus tard, en 1995, la France a
        demandé
        l'inscription du télétravail à l'ordre du jour du sommet du G7 sur la société de l'information. Le télétravail
        est
        présenté comme un atout économique et social.
      </p>

      <p> Après les années 2000…</p>
      <p>
        Au niveau européen, en 2002, un premier accord est signé et mis en œuvre de façon autonome par les partenaires
        sociaux pour renforcer les conditions et la sécurités et répondre aux besoins des employeurs et des employés
        dans
        le télétravail. Cet accord permet aux télétravailleurs de bénéficier des mêmes droits qu’aux travailleurs
        effectuant leur activités dans les locaux de l’entreprise.
      </p>
      <p>
        Au niveau national, en 2005, les partenaires sociaux français interviennent dans le même sens en signant un
        accord
        national interprofessionnel (ANI) relatif au télétravail. Conforté en 2012 par la loi Warsmann II, la notion du
        télétravail est enfin introduite dans le code du travail. En 2017, l'ordonnance n°2017-1387 du 22 septembre 2017
        du Président de la République Emmanuel Macron a reconnu définitivement le télétravail comme un mode de travail
        qui
        a redéfini le télétravail avec le statut et les droits du télétravailleur et ses modalités de mise en œuvre.
      </p>
